package Day4;

public class Parse {

	public static void main(String[] args) {
		System.out.println(parse(true));
	}
	
	public static String parse(long value) {
		return ""+value;
	}
	public static String parse(double value) {
		return ""+value;
	}
	public static String parse(char value) {
		return ""+value;
	}
	public static String parse(boolean value) {
		return ""+value;
	} 

}
